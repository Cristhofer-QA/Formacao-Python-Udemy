adicao = 10 + 10.2
print('Adição', adicao)

subtracao = 10 - 5
print('Subtração', subtracao)

multiplicacao = 5 * 12
print('Multiplicação', multiplicacao)

divisao = 10 / 2.2  # Sempre o retorno será um float
print('Divisão', divisao)

divisao_inteira = 10.0 // 3  # Sempre retornará o valor truncado (apenas o quociente). Caso o um dos valores sera float, então retornara o quociente .0 (4.0, por ex.)
print('Divisão inteira', divisao_inteira)

potencia = 2 ** 10
print('Potência', potencia)

modulo = 55 % 4  # Resto da divisão
print('Módulo', modulo)